### Perulangan

#List
angka_list=[0,1,2,3,4,5]
for i in angka_list:
    print(f"Angka sekarang: {i}")
print()

#Range
for i in range(5):
    print(f"Angka sekarang: {i}")
print()

angka_range=range(10)
for i in angka_range:
    print(f"Angka sekarang: {i}")
print()

angka_range=range(1,10)
for i in angka_range:
    print(f"Angka sekarang: {i}")
print()

#String
strings = "Im the best!"
