## Fix some Phrases:

# \ on ' to become string
print('G\'day isn\'t?')

#backslash
print("C:\\user\\Bram")

#tab
print("bram\t\tjauhan sama\t\tsaya")

#backspace
print("Ibrahim\b\b\b\b\b\bbramullah")

# newline (\n, \r, \r\n)
print("#1\n#2")
print("#1\r#2")
print("#1\r\n#2")

# raw string
print(r'Hi\Hello \b\r\t\\')

# multiple 1 quote raw string
print(r'''
I'am Batman
Waw
''')

# multiple 2 quote raw string
print(r"""
I'am Batman
Waw
""")
